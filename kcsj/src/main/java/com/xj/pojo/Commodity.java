package com.xj.pojo;
	/* 商品表 */
public class Commodity {
	private Integer id;
	private String commname;
	private String commdate;
	private String commproduct;
	private String commno;
	private float commprice;
	private Integer commrepertory;
	private String commtype;
	private String commimg;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCommname() {
		return commname;
	}
	public void setCommname(String commname) {
		this.commname = commname;
	}
	public String getCommdate() {
		return commdate;
	}
	public void setCommdate(String commdate) {
		this.commdate = commdate;
	}
	public String getCommproduct() {
		return commproduct;
	}
	public void setCommproduct(String commproduct) {
		this.commproduct = commproduct;
	}
	public String getCommno() {
		return commno;
	}
	public void setCommno(String commno) {
		this.commno = commno;
	}
	public float getCommprice() {
		return commprice;
	}
	public void setCommprice(float commprice) {
		this.commprice = commprice;
	}
	public Integer getCommrepertory() {
		return commrepertory;
	}
	public void setCommrepertory(Integer commrepertory) {
		this.commrepertory = commrepertory;
	}
	public String getCommtype() {
		return commtype;
	}
	public void setCommtype(String commtype) {
		this.commtype = commtype;
	}
	public String getCommimg() {
		return commimg;
	}
	public void setCommimg(String commimg) {
		this.commimg = commimg;
	}
	
}
